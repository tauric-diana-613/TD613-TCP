const BLIP_DETECTION_PATTERN_CACHE = new Map();

function blipEscapeRegex(value = '') {
  return String(value || '').replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function blipTokenPattern(token = '') {
  const raw = String(token || '').trim();
  const escaped = blipEscapeRegex(raw);
  const prefix = /^[A-Za-z0-9]/.test(raw) ? '\\b' : '';
  const suffix = /[A-Za-z0-9]$/.test(raw) ? '\\b' : '';
  return `${prefix}${escaped}${suffix}`;
}

function blipDetectionRule(entry) {
  const variants = [entry.token, ...(entry.variants || [])].filter(Boolean);
  const key = variants.join('|');
  if (!BLIP_DETECTION_PATTERN_CACHE.has(key)) {
    BLIP_DETECTION_PATTERN_CACHE.set(key, new RegExp(variants.map(blipTokenPattern).join('|'), 'gi'));
  }
  return Object.freeze({
    id: `blip:${entry.token.toLowerCase()}`,
    pattern: BLIP_DETECTION_PATTERN_CACHE.get(key)
  });
}

function blipActiveRewriteEntries() {
  const contextSafeTokens = new Set([
    'ion',
    'finna',
    'tryna',
    'talmbout',
    'frfr',
    'bffr',
    'rn',
    'irl',
    'perf',
    'abt',
    'b4',
    'w/',
    'w/o'
  ]);
  return BLIP_SHORTHAND_ONTOLOGY.filter((entry) =>
    entry.rewriteStatus === 'active' ||
    contextSafeTokens.has(String(entry.token || '').toLowerCase())
  );
}

function blipFormalizationRule(entry) {
  const variants = [entry.token, ...(entry.variants || [])].filter(Boolean);
  return Object.freeze({
    family: entry.featureFamily,
    pattern: new RegExp(variants.map(blipTokenPattern).join('|'), 'gi'),
    replacement: entry.canonicalExpansion,
    label: `blip:${entry.token}->${entry.canonicalExpansion}`
  });
}

function blipDegradationRule(entry) {
  return Object.freeze({
    family: entry.featureFamily,
    pattern: new RegExp(blipTokenPattern(entry.canonicalExpansion), 'gi'),
    replacement: entry.token,
    label: `blip:${entry.canonicalExpansion}->${entry.token}`,
    requiresMarker: `blip:${entry.token.toLowerCase()}`
  });
}

export const BLIP_VERNACULAR_DETECTION_RULES = Object.freeze({
  orthographyNoise: Object.freeze([]),
  chatspeakShorthand: Object.freeze(BLIP_SHORTHAND_ONTOLOGY.filter((entry) => entry.featureFamily === 'chatspeakShorthand').map(blipDetectionRule)),
  notePosture: Object.freeze(BLIP_SHORTHAND_ONTOLOGY.filter((entry) => entry.featureFamily === 'notePosture').map(blipDetectionRule)),
  slangMarkers: Object.freeze(BLIP_SHORTHAND_ONTOLOGY.filter((entry) => entry.featureFamily === 'slangMarkers').map(blipDetectionRule)),
  vernacularMarkers: Object.freeze(BLIP_SHORTHAND_ONTOLOGY.filter((entry) => entry.featureFamily === 'vernacularMarkers').map(blipDetectionRule))
});

export const BLIP_FORMALIZATION_FEATURE_RULES = Object.freeze(blipActiveRewriteEntries().map(blipFormalizationRule));

export const BLIP_DEGRADATION_FEATURE_RULES = Object.freeze(blipActiveRewriteEntries()
  .filter((entry) => entry.rewriteStatus === 'active' || ['ion', 'finna', 'tryna', 'talmbout', 'frfr', 'bffr', 'rn', 'irl', 'perf', 'b4'].includes(entry.token.toLowerCase()))
  .filter((entry) => !entry.flags.includes('self-harm-or-hostile'))
  .map(blipDegradationRule));

export function summarizeBlipOntology() {
  const byFamily = BLIP_SHORTHAND_ONTOLOGY.reduce((acc, entry) => {
    acc[entry.featureFamily] = (acc[entry.featureFamily] || 0) + 1;
    return acc;
  }, {});
  return Object.freeze({
    entryCount: BLIP_SHORTHAND_ONTOLOGY.length,
    activeRewriteCount: BLIP_FORMALIZATION_FEATURE_RULES.length,
    degradationRewriteCount: BLIP_DEGRADATION_FEATURE_RULES.length,
    byFamily: Object.freeze(byFamily)
  });
}
