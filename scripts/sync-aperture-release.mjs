#!/usr/bin/env node
import { spawnSync } from 'node:child_process';
import path from 'node:path';
import {
  APERTURE_ENGINE_PATH,
  APERTURE_RELEASE_PATH,
  REPO_ROOT,
  readText,
  updateApertureEngineJs,
  writeText
} from './lib/aperture-sync-lane.mjs';

const release = JSON.parse(await readText(APERTURE_RELEASE_PATH));
const metadata = {
  version: release.version,
  schema: release.apertureSchema,
  featureVersion: release.featureVersion,
  doctrineKernelSchema: release.doctrineKernelSchema
};

await writeText(
  APERTURE_ENGINE_PATH,
  updateApertureEngineJs(await readText(APERTURE_ENGINE_PATH), metadata)
);

const jsReleasePath = path.join(REPO_ROOT, 'app', 'aperture', 'release.js');
const pythonReleasePath = path.join(REPO_ROOT, 'packages', 'dome_world_exact', 'release.py');
const fixturePath = path.join(REPO_ROOT, 'tests', 'fixtures', 'aperture-release.json');

await writeText(
  jsReleasePath,
  `// Generated by npm run sync:aperture-release. Do not edit manually.\nexport const APERTURE_RELEASE = Object.freeze(${JSON.stringify(release, null, 2)});\n`
);
await writeText(
  pythonReleasePath,
  `"""Generated by npm run sync:aperture-release. Do not edit manually."""\n\n` +
  `APERTURE_VERSION = ${JSON.stringify(release.version)}\n` +
  `APERTURE_SCHEMA = ${JSON.stringify(release.apertureSchema)}\n` +
  `APERTURE_FEATURE_VERSION = ${JSON.stringify(release.featureVersion)}\n` +
  `APERTURE_DOCTRINE_SCHEMA = ${JSON.stringify(release.doctrineKernelSchema)}\n` +
  `APERTURE_DOME_BRIDGE_SCHEMA = ${JSON.stringify(release.domeBridgeSchema)}\n` +
  `APERTURE_DOME_DIAGNOSTIC_RECEIPT_SCHEMA = ${JSON.stringify(release.domeDiagnosticReceiptSchema)}\n` +
  `FLOWCORE_CONTEXT_RECEIPT_SCHEMA = ${JSON.stringify(release.flowCoreContextReceiptSchema)}\n` +
  `APERTURE_ROUND_TRIP_RECEIPT_SCHEMA = ${JSON.stringify(release.roundTripReceiptSchema)}\n` +
  `APERTURE_DOME_BRIDGE_POSTURE = ${JSON.stringify(release.bridgePosture)}\n` +
  `DOME_WORLD_VERSION = ${JSON.stringify(release.domeWorld?.version || 'v0.4.3')}\n` +
  `DOME_WORLD_SCHEMA = ${JSON.stringify(release.domeWorld?.schema || 'td613.dome-world/v0.4.3')}\n` +
  `EORFD_OPERATIONAL_STATE = ${JSON.stringify(release.eorfd?.operationalState || 'interface_context')}\n` +
  `EORFD_CLAIM_AUTHORITY = ${JSON.stringify(release.eorfd?.claimAuthority || 'design_signal')}\n`
);
await writeText(fixturePath, `${JSON.stringify(release, null, 2)}\n`);

const generated = spawnSync(process.execPath, ['scripts/generate-browser-engine.mjs'], {
  cwd: REPO_ROOT,
  encoding: 'utf8'
});
if (generated.status !== 0) {
  process.stderr.write(generated.stderr || generated.stdout || 'browser-engine generation failed\n');
  process.exit(generated.status || 1);
}

console.log(JSON.stringify({
  status: 'synced',
  version: release.version,
  schema: release.apertureSchema,
  featureVersion: release.featureVersion,
  targets: [
    'app/engine/td613-aperture.js',
    'app/aperture/release.js',
    'packages/dome_world_exact/release.py',
    'tests/fixtures/aperture-release.json',
    'app/browser-engine.js'
  ]
}, null, 2));
